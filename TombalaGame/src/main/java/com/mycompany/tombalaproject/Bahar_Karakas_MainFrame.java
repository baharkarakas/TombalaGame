    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        for(int i = 0; i < players.getSize(); i++) {
            players.getAt(i).gameStarted = true;
            players.getAt(i).autoDraw = isAutoMark();
        }
        Bahar_Karakas_TombalaGame game = new Bahar_Karakas_TombalaGame();
        game.setPlayers(players, this);
game.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_jButton2ActionPerformed

    @Override
    public void setVisible(boolean b) {
        super.setVisible(b); 
        
        if(b) {
        Bahar_Karakas_TombalaLinkedList<Bahar_Karakas_TombalaCard> oldPlayerList = players;
       
        for(int i = 0; i < oldPlayerList.getSize(); i++) {
            oldPlayerList.getAt(i).setDisposeCallback(new Bahar_Karakas_OnDispose() {
                @Override
                public void onPlayerDispose() {
                    
                }
            });
            oldPlayerList.getAt(i).dispose();
        }
        
        players = new Bahar_Karakas_TombalaLinkedList<>();
        
        jButton2.setEnabled(false);
        jLabel2.setVisible(true);
        }
    }

    
    
