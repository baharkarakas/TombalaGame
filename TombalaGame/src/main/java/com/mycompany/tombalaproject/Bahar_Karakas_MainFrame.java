    static Random random = new Random();
    
    
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        Bahar_Karakas_TombalaLinkedList<Bahar_Karakas_TombalaNumber> cardNumbers = new Bahar_Karakas_TombalaLinkedList<>();
        
        for (int i = 0; i < 3; i++) {
            for (int x = 0; x < 5; x++) {
                
                int maxBoundry = (5+x)*10;
                
                int num = random.nextInt((x == 0) ? 1 : ((Math.floorDiv(cardNumbers.getLastElement().number, 10) + 1) * 10), maxBoundry);
                Bahar_Karakas_TombalaNumber number = new Bahar_Karakas_TombalaNumber(num);
                if (cardNumbers.contains(number)) {
                    x--;
                    continue;
                }
                
                cardNumbers.addToList(number);
            }
        }
        
        
        
       sortArray(cardNumbers);
       sortArray(cardNumbers);
        
        
        int possibleName = 0;
        for(var x = 0; x < players.getSize(); x++) {
            int thisName = players.getAt(x).name;
            if(possibleName <= thisName) possibleName = thisName + 1;
        }
        
        final Bahar_Karakas_TombalaCard card = new Bahar_Karakas_TombalaCard();
        players.addToList(card);
        card.setVisible(true);
        card.setNumbers(cardNumbers);
        card.setPlayerName(possibleName);
        
        card.setOnWinAction( new Bahar_Karakas_OnDispose() {
            @Override
            public void onPlayerDispose() {
                setWinner(card);
            }
        });
        
        card.setDisposeCallback(new Bahar_Karakas_OnDispose() {
            @Override
            public void onPlayerDispose() {
                
                players.removeFromList(card);
                
                if(!gameStarted && players.getSize() <= 0) {
                    jButton2.setEnabled(false);
                    jLabel2.setVisible(true);
                }
            }
        });
       
        jButton2.setEnabled(true);
        jLabel2.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

    void sortArray(Bahar_Karakas_TombalaLinkedList<Bahar_Karakas_TombalaNumber> cardNumbers) {
         for(int i = 0; i < cardNumbers.getSize(); i++) {
            
            Bahar_Karakas_TombalaNumber myNumber = cardNumbers.getAt(i);
        
            for(int x = i; x < cardNumbers.getSize(); x++) {
                if(x == i) continue;
                 Bahar_Karakas_TombalaNumber compareNumber = cardNumbers.getAt(x);
                 
                 
                 if(Math.floor(myNumber.number/10) == Math.floor(compareNumber.number/10) && myNumber.number > compareNumber.number) {
                     System.out.println("Switching " + myNumber.number + " with " + compareNumber.number);
                        cardNumbers.replaceElement(x, myNumber);
                     cardNumbers.replaceElement(i, compareNumber);
                     break;
                 }
            }
        }
    }
    
    public void setWinner(Bahar_Karakas_TombalaCard winner) {
        for(int i = 0; i < players.getSize(); i++) {
            players.getAt(i).setWinner(winner);
        }
        
        
    }
    
